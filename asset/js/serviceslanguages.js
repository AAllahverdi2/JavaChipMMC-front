document.querySelectorAll(".home-custom-select-option").forEach(item => {
    item.addEventListener("click", function () {
        let lang = this.getAttribute("data-lang");
        changeWebsiteLanguage(lang);
    });
});

function changeWebsiteLanguage(lang) {
    if (!websiteTranslations || !websiteTranslations[lang]) {
        return;
    }
    localStorage.setItem('selectedLanguage', lang);

    const elements = document.querySelectorAll("[data-key]");
    elements.forEach((element) => {
        const key = element.getAttribute("data-key");
        if (websiteTranslations[lang] && websiteTranslations[lang][key]) {
            element.textContent = websiteTranslations[lang][key];
        } else {
        }
    });

    closeLanguageMenu();
}

function toggleHomeDropdown() {
    const languageMenu = document.getElementById("languageMenu");
    if (!languageMenu) {
        return;
    }
    languageMenu.style.display = (languageMenu.style.display === "block") ? "none" : "block";
}

function closeLanguageMenu() {
    const languageMenu = document.getElementById("languageMenu");
    if (!languageMenu) {
        return;
    }
    languageMenu.style.display = "none";
}
